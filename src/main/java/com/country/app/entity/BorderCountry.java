package com.country.app.entity;

public class BorderCountry {

	private String countryName;
	private String commonLanguage;
	private int drivingDistance;
	private String carDriving;
	private String independent;
	private String unionMember;
	public String getCountryName() {
		return countryName;
	}
	public void setCountryName(String countryName) {
		this.countryName = countryName;
	}
	public String getCommonLanguage() {
		return commonLanguage;
	}
	public void setCommonLanguage(String commonLanguage) {
		this.commonLanguage = commonLanguage;
	}
	public int getDrivingDistance() {
		return drivingDistance;
	}
	public void setDrivingDistance(int drivingDistance) {
		this.drivingDistance = drivingDistance;
	}
	public String getCarDriving() {
		return carDriving;
	}
	public void setCarDriving(String carDriving) {
		this.carDriving = carDriving;
	}
	public String getIndependent() {
		return independent;
	}
	public void setIndependent(String independent) {
		this.independent = independent;
	}
	public String getUnionMember() {
		return unionMember;
	}
	public void setUnionMember(String unionMember) {
		this.unionMember = unionMember;
	}
	
	
	
	
	


}
